def render_clean_html(text):
    """
    ×× ×•×¢ ×¨×™× ×“×•×¨ ×˜×§×¡×˜ ×—×›×: ××–×”×” ×›×•×ª×¨×•×ª ×•×”×“×’×©×•×ª ×•××›×¨×™×— ×™×¨×™×“×ª ×©×•×¨×” ×•×¢×™×¦×•×‘ × ×§×™.
    """
    if not text: return ""
    html = text.replace("\n", "<br>")
    lines = html.split("<br>")
    formatted = []
    in_list = False
    
    for line in lines:
        cl = line.strip()
        if not cl: continue
        
        # ×›×•×ª×¨×•×ª
        if cl.startswith("###"):
            if in_list: formatted.append("</ul>"); in_list = False
            cl = f"<h3 style='color:#0277bd; border-bottom:2px solid #e1f5fe; margin-top:25px;'>{cl.replace('###','')}</h3>"
        elif cl.startswith("##"):
            if in_list: formatted.append("</ul>"); in_list = False
            cl = f"<h2 style='color:#1565c0; background:#e3f2fd; padding:10px; border-radius:5px;'>{cl.replace('##','')}</h2>"
        
        # ×˜×™×¤×•×œ ×—×›× ×‘×”×“×’×©×•×ª (Bold) - ×™×¨×™×“×ª ×©×•×¨×”
        elif "**" in cl and cl.startswith("**"):
            parts = cl.split("**")
            # parts[1] ×”×•× ×”××•×“×’×©, parts[2] ×”×•× ×”×”××©×š
            if len(parts) >= 3:
                cl = f"""
                <div style='margin-bottom:8px; line-height:1.6;'>
                    <span style='font-weight:900; color:#b71c1c; font-size:1.05em; display:block; margin-bottom:2px;'>ğŸ“Œ {parts[1]}</span>
                    <span style='color:#37474f;'>{parts[2]}</span>
                </div>"""
            else:
                cl = cl.replace("**", "<b>").replace("**", "</b>")

        # ×¨×©×™××•×ª
        elif cl.startswith("* ") or cl.startswith("- "):
            if not in_list: formatted.append("<ul style='margin-right:20px;'>"); in_list = True
            content = cl[2:]
            # ×”×“×’×©×” ×‘×ª×•×š ×¨×©×™××”
            if "**" in content:
                parts = content.split("**")
                if len(parts) >= 3:
                    content = f"<strong>{parts[1]}</strong> {parts[2]}"
            cl = f"<li style='margin-bottom:5px;'>{content}</li>"
        
        else:
            if in_list: formatted.append("</ul>"); in_list = False
            cl = f"<div style='margin-bottom:5px;'>{cl}</div>"
            
        formatted.append(cl)
    
    if in_list: formatted.append("</ul>")
    return "".join(formatted)

